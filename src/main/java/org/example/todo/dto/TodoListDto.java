package org.example.todo.dto;

public class TodoListDto {
    Long id;
    String title;

    public TodoListDto() {
    }

    public TodoListDto(Long id, String title) {
        this.id = id;
        this.title = title;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
